/**
 * PIKIT PikitQuest v2 Deployment Script
 * Deploys ONLY the updated PikitQuest contract to Sepolia
 * (MockUSDC and PikitVault are unchanged)
 */

'use strict';

require('dotenv').config({ path: require('path').resolve(__dirname, '../.env') });

const fs   = require('fs');
const path = require('path');
const solc = require('solc');
const { ethers } = require('ethers');

// ---------------------------------------------------------------------------
// Config
// ---------------------------------------------------------------------------
const PRIVATE_KEY = process.env.DEPLOYER_PRIVATE_KEY;
if (!PRIVATE_KEY) {
  console.error('ERROR: DEPLOYER_PRIVATE_KEY not set in .env');
  process.exit(1);
}

const RPC_CANDIDATES = [
  process.env.SEPOLIA_RPC_URL,
  'https://ethereum-sepolia-rpc.publicnode.com',
  'https://1rpc.io/sepolia',
  'https://rpc2.sepolia.org',
].filter(Boolean);

const CONTRACTS_DIR = path.resolve(__dirname, '../contracts');
const PUBLIC_JS_DIR = path.resolve(__dirname, '../public/js');

// ---------------------------------------------------------------------------
// Solidity compilation
// ---------------------------------------------------------------------------
function compile() {
  const source = fs.readFileSync(path.join(CONTRACTS_DIR, 'PikitQuest.sol'), 'utf8');

  const input = {
    language: 'Solidity',
    sources: { 'PikitQuest.sol': { content: source } },
    settings: {
      outputSelection: { '*': { '*': ['abi', 'evm.bytecode.object'] } },
      optimizer: { enabled: true, runs: 200 },
    },
  };

  const output = JSON.parse(solc.compile(JSON.stringify(input)));

  if (output.errors) {
    const fatal = output.errors.filter(e => e.severity === 'error');
    if (fatal.length > 0) {
      console.error('Solidity compilation errors:');
      fatal.forEach(e => console.error(' ', e.formattedMessage));
      process.exit(1);
    }
    output.errors
      .filter(e => e.severity === 'warning')
      .forEach(e => console.warn('[warn]', e.formattedMessage));
  }

  const contractData = output.contracts['PikitQuest.sol']['PikitQuest'];
  return {
    abi:      contractData.abi,
    bytecode: '0x' + contractData.evm.bytecode.object,
  };
}

// ---------------------------------------------------------------------------
// Provider selection
// ---------------------------------------------------------------------------
async function getProvider() {
  for (const url of RPC_CANDIDATES) {
    console.log(`Trying RPC: ${url}`);
    try {
      const provider = new ethers.JsonRpcProvider(url);
      const network  = await Promise.race([
        provider.getNetwork(),
        new Promise((_, reject) => setTimeout(() => reject(new Error('timeout')), 10000)),
      ]);
      if (network.chainId !== 11155111n) {
        console.warn(`  Chain ID mismatch: expected 11155111, got ${network.chainId}`);
        continue;
      }
      console.log(`  Connected to Sepolia via ${url}`);
      return provider;
    } catch (err) {
      console.warn(`  Failed (${err.message})`);
    }
  }
  throw new Error('All RPC endpoints failed.');
}

// ---------------------------------------------------------------------------
// Update quest-abi.js with new address and ABI
// ---------------------------------------------------------------------------
function updateQuestAbi(address, abi) {
  const filePath = path.join(PUBLIC_JS_DIR, 'quest-abi.js');

  // Build a minimal ABI with only the functions/events we use
  const minimalAbi = abi.filter(item => {
    if (item.type === 'event') return item.name === 'QuestCompleted';
    if (item.type === 'function') {
      return ['completeQuest', 'isCompleted', 'batchIsCompleted'].includes(item.name);
    }
    return false;
  });

  const content = `// PikitQuest Contract ABI — Sepolia testnet
// Auto-generated by scripts/deploy-quest-v2.js
window.PIKIT_QUEST = {
  CONTRACT_ADDRESS: '${address}',
  CHAIN_ID: 11155111, // Sepolia

  ABI: ${JSON.stringify(minimalAbi, null, 4)}
};
`;
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`Updated ${filePath}`);
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
async function main() {
  console.log('=== PikitQuest v2 Deployment ===\n');

  // 1. Compile
  console.log('Compiling PikitQuest...');
  const { abi, bytecode } = compile();
  console.log('  Compiled successfully');

  // 2. Connect
  const provider = await getProvider();
  const wallet   = new ethers.Wallet(PRIVATE_KEY, provider);
  console.log(`\nDeployer address: ${wallet.address}`);

  const balance = await provider.getBalance(wallet.address);
  console.log(`Deployer balance: ${ethers.formatEther(balance)} ETH`);
  if (balance < ethers.parseEther('0.005')) {
    console.warn('WARNING: Balance may be too low for deployment.');
  }

  // 3. Deploy
  console.log('\nDeploying PikitQuest v2...');
  const factory  = new ethers.ContractFactory(abi, bytecode, wallet);
  const contract = await factory.deploy();
  console.log(`  Tx hash: ${contract.deploymentTransaction().hash}`);
  console.log('  Waiting for confirmation...');

  await Promise.race([
    contract.waitForDeployment(),
    new Promise((_, reject) => setTimeout(() => reject(new Error('confirmation timeout (120s)')), 120000)),
  ]);

  const address = await contract.getAddress();
  console.log(`  PikitQuest v2 deployed at: ${address}`);

  // 4. Update ABI file
  console.log('\nUpdating quest-abi.js...');
  updateQuestAbi(address, abi);

  // 5. Summary
  console.log('\n=== Deployment Complete ===');
  console.log(`  PikitQuest v2: ${address}`);
  console.log(`  https://sepolia.etherscan.io/address/${address}`);
}

main().catch(err => {
  console.error('\nDeployment failed:', err.message);
  process.exit(1);
});
